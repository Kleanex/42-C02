char	*ft_strlowcase(char *str)
{
	int	j;

	j = 0;
	while (str[j] != '\0')
	{
		while ((str[j] > 31 && str[j] <= 64) && (str[j] > 90 && str[j] < 127))
			str[j] = str[j];
		while (str[j] >= 65 && str[j] <= 90)
			str[j] = str[j] +32;
	j++;
	}
	return (str);
}
