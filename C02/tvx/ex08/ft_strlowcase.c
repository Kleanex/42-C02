char	*ft_strlowcase(char *str)
{
	int	j;

	j = 0;
	while ((str[j] >= 96 && str[j] <= 122) || (str[j] >= 65 && str[j] <= 90))
	{
		if (str[j] >= 65 && str[j] <= 90)
		{
			str[j] = str[j] + 32;
			j++;
		}
		if (str[j] >= 96 && str[j] <= 122)
		{
			str[j] = str[j];
			j++;
		}
	}
	return (str);
}
