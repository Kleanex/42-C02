int	ft_str_is_alpha(char *str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i] != '\0')
		i++;
	j = 0;
	while ((str[j] >= 'A' && str[j] <= 'Z')
		|| (str[j] >= 'a' && str[j] <= 'z'))
		j++;
	if (j == i)
		return (1);
	else
		return (0);
}
